// Ejemplo de uso de operadores aritméticos
public class OperadoresAritmeticos {
    public static void main(String[] args) {
        System.out.println("Ejemplo de operadores aritméticos");
        int int1 = 10000;
        int int2 = 3;
        double real1 = 10000.0;

        System.out.println("int1 + int2: " + (int1 + int2));
        System.out.println("int1 - int2: " + (int1 - int2));
        System.out.println("int1 / int2: " + (int1 / int2));
        System.out.println("real1 / int2: " + (real1 / int2));
        System.out.println("int1 * int2: " + (int1 * int2));
        System.out.println("int1 % int2: " + (int1 % int2));
        System.out.println("int2 % int1: " + (int2 % int1));
    }
}
